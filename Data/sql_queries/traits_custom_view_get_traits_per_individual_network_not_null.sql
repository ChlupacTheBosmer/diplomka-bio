SELECT 
    sp_code, 
    season, 
    elevation,
	species,
	shape_upd,
	symmetry,
	flower_pos,
	anther_pos,
	odour,
	brightness,
	colour,
	nectar_guides,
	size,
	tube_length,
	volume,
	concentration,
	sugar_amount,
	glucose,
	fructose,
	saccharose
FROM 
    traits_unique_per_network_not_null
WHERE
	season == "DRY" AND elevation == 650